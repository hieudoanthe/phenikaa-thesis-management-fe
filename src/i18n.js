const saved = (() => {
  try {
    // Use global settings - not user-specific
    const storageKey = "app_settings_global";

    const settings = localStorage.getItem(storageKey);
    if (settings) {
      const parsed = JSON.parse(settings);
      if (parsed?.appearance?.language) {
        return parsed.appearance.language;
      }
    }

    // Fallback to old system for backward compatibility
    const roleSettings =
      localStorage.getItem("settings_student_v1") ||
      localStorage.getItem("settings_teacher_v1") ||
      localStorage.getItem("settings_admin_v1");

    if (roleSettings) {
      const parsed = JSON.parse(roleSettings);
      const lang = parsed?.appearance?.language || null;
      if (lang) {
        return lang;
      }
    }

    const raw = localStorage.getItem("student_settings_v1");
    if (raw) {
      const parsed = JSON.parse(raw);
      const lang = parsed?.appearance?.language || null;
      if (lang) {
        return lang;
      }
    }

    return null;
  } catch (error) {
    return null;
  }
})();

